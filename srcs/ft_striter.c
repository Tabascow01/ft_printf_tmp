#include "ft_printf.h"

void	ft_striter(char *str, void (*f)(char *))
{
	int i;

	i = 0;
	if (str == NULL || f == NULL)
		return ;
	while (str[i])
	{
		f(&str[i]);
		i++;
	}
}
